package com.bridgelabz.util;

public class Patient 
{
	private int id;
	private String name;
	private int age;
	private long phNo;
	
	Patient()
	{
		id=0;
		name=null;
		age=0;
		phNo=0;
	}

	public int  getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public long getPhNo() {
		return phNo;
	}

	public void setPhNo(long phNo) {
		this.phNo = phNo;
	}
	
	
	
	
	
	
	
	
	

}
